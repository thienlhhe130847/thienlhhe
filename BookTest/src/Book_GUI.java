    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String code = tftBookCode.getText();
        String name = tftBookName.getText();
        String Author = tftAuthor.getText();
        String Publisher = tftPublisher.getText();
        int publisher_year = Integer.parseInt(cbxyear.getSelectedItem().toString());
        boolean forRent = cbRent.isSelected();
        BookObject b = new BookObject(code, name, Author, Publisher, publisher_year, forRent);
        if(c.checkEmpty(name) == true && c.checkEmpty(code) == true && c.checkEmpty(Publisher) == true
                && c.checkEmpty(Author) == true) {
              if(c.checkExist(code) != -1) {
                 if(c.checkBookChange(b) == true) {
                     listBook.setElementAt(name,c.checkExist(code));
                     c.updateBook(b);
                 }
                 else {
                     JOptionPane.showMessageDialog(null, "Must be change information to Save");
                 }
              }
              else {
                  c.addBook(b);
                  listBook.addElement(name);
              }
        }
        else {
            JOptionPane.showMessageDialog(null, "Input valid data");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

