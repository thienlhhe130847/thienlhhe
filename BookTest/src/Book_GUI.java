    private void display(int select) {
        try {
            BookObject b = c.getListBook().get(select);
            tftBookCode.setText(b.getBookCode());
            tftBookName.setText(b.getBookName());
            tftAuthor.setText(b.getAuthor());
            tftPublisher.setText(b.getPublisher());
            cbxyear.setSelectedItem(b.getYear());
            cbRent.setSelected(b.isRent());
        }
        catch (ArrayIndexOutOfBoundsException e ) {
        }
    }

